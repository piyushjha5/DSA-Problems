import java.util.*;
public class Operators {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);

        // AND &
        System.out.println(5 & 6);

        // OR |
        System.out.println(5 | 6);

        // XOR ^
        System.out.println(5 ^ 6);

        // 1's Complement ~
        System.out.println(~5);

        // Left Shift <<
        System.out.println(5 << 1);

        // Right Shift >>
        System.out.println(5 >> 1);
    }
}